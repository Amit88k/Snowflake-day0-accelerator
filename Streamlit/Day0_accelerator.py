def _dbg(msg):
    """Append a timestamped debug message to the session debug log."""
    ts = datetime.now().strftime("%H:%M:%S.%f")[:-3]
    st.session_state.setdefault("_debug_log", []).append(f"[{ts}] {msg}")


def resolve_name(project, env, suffix, naming):
    """Build a Snowflake object name respecting the chosen naming convention.

    Args:
        project: project prefix   (e.g. "SALES_ANALYTICS")
        env:     environment code (e.g. "DEV")
        suffix:  object suffix    (e.g. "DB", "WH", "MONITOR")
        naming:  naming template  ("<PROJECT>_<ENV>_<OBJECT>" or
                                   "<ENV>_<PROJECT>_<OBJECT>")
    Returns:
        Fully resolved name, e.g. "SALES_ANALYTICS_DEV_DB".
    """
    if naming == "<PROJECT>_<ENV>_<OBJECT>":
        return project + "_" + env + "_" + suffix
    return env + "_" + project + "_" + suffix


