def run_validation(validation_sql):
    """Execute each validation statement and return a list of result dicts
    with keys: object, status (FOUND/MISSING/ERROR), detail.
    """
    _dbg("Running validation queries")
    stmts = [s.strip() for s in validation_sql.split(";") if s.strip() and not s.strip().startswith("--")]
    results = []
    for stmt in stmts:
        try:
            rows = session.sql(stmt).collect()
            row_count = len(rows)
            if stmt.strip().upper().startswith("SELECT"):
                found_val = rows[0][1] if rows and len(rows[0]) > 1 else 0
                obj_name = rows[0][0] if rows else stmt[:60]
                results.append({"object": str(obj_name), "status": "FOUND" if int(found_val) > 0 else "MISSING", "detail": str(found_val) + " match(es)"})
            elif stmt.strip().upper().startswith("SHOW"):
                obj_desc = stmt.replace("SHOW ", "").replace(";", "")[:60]
                results.append({"object": obj_desc, "status": "FOUND" if row_count > 0 else "MISSING", "detail": str(row_count) + " result(s)"})
        except Exception as e:
            results.append({"object": stmt[:60], "status": "ERROR", "detail": str(e)[:200]})
    return results


