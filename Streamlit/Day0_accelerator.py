def execute_with_retry(stmt, retries=2):
    """Execute a single SQL statement with up to *retries* retry attempts."""
    for attempt in range(retries + 1):
        try:
            session.sql(stmt).collect()
            return {"success": True}
        except Exception as e:
            _dbg(f"Statement failed (attempt {attempt+1}/{retries+1}): {str(e)[:120]}")
            if attempt == retries:
                return {"success": False, "error": str(e)}
            time.sleep(1)


def log_audit(skill, model, prompt, sql, reviewed, deployed, status):
    """Insert an audit record into the skill audit log table (best-effort)."""
    try:
        safe_prompt = prompt.replace("'", "''")[:2000]
        safe_sql = sql.replace("'", "''")[:5000]
        session.sql(
            "INSERT INTO SKILL_DB.SKILLS.SKILL_AUDIT_LOG "
            "(SKILL_NAME, MODEL_USED, USER_PROMPT, GENERATED_SQL, WAS_APPROVED, WAS_EXECUTED, EXECUTION_STATUS) "
            "VALUES ('" + skill + "', '" + model + "', '" + safe_prompt + "', '" + safe_sql + "', "
            + str(reviewed).upper() + ", " + str(deployed).upper() + ", '" + status + "')"
        ).collect()
    except Exception as exc:
        _dbg(f"Audit log insert failed: {exc}")
        pass


def execute_sql_scripts(sql_text, deployment_id):
    """Parse and execute all SQL statements with a live progress bar.

    Returns a result dict with status, counts, duration, errors, and log.
    """
    _dbg(f"Starting deployment {deployment_id}")
    stmts = parse_sql_statements(sql_text)
    total = len(stmts)
    _dbg(f"Parsed {total} statements")
    if total == 0:
        return {"status": "FAILED", "total": 0, "successful": 0, "failed": 0, "duration": 0, "errors": [], "log": []}
    pb = st.progress(0)
    stxt = st.empty()
    metrics_html = st.empty()
    logc = st.expander("Execution Log", expanded=True)
    ok, fail, errs, elog, t0 = 0, 0, [], [], time.time()
    for i, stmt in enumerate(stmts):
        sm = get_stmt_summary(stmt)
        cat = categorize_statement(stmt)
        stxt.info("Exec " + str(i + 1) + "/" + str(total) + ": " + sm)
        pb.progress((i + 1) / total)
        r = execute_with_retry(stmt)
        el = round(time.time() - t0, 1)
        if r["success"]:
            ok += 1
            elog.append({"index": i + 1, "status": "OK", "category": cat, "summary": sm, "elapsed": el})
            with logc:
                st.text("OK: " + sm)
        else:
            fail += 1
            err_msg = r.get("error", "")[:300]
            errs.append({"statement": sm, "error": err_msg, "category": cat})
            elog.append({"index": i + 1, "status": "FAIL", "category": cat, "summary": sm, "error": err_msg, "elapsed": el})
            with logc:
                st.text("FAIL: " + sm)
        remaining = total - i - 1
        metrics_html.markdown(
            '<div class="deploy-metrics">'
            '<div class="deploy-metric ok"><div class="value">' + str(ok) + '</div><div class="label">Success</div></div>'
            '<div class="deploy-metric fail"><div class="value">' + str(fail) + '</div><div class="label">Failed</div></div>'
            '<div class="deploy-metric"><div class="value">' + str(remaining) + '</div><div class="label">Remaining</div></div>'
            '<div class="deploy-metric time"><div class="value">' + str(el) + 's</div><div class="label">Elapsed</div></div>'
            '</div>', unsafe_allow_html=True)
    d = round(time.time() - t0, 1)
    pb.progress(1.0)
    stxt.empty()
    s = "SUCCESS" if fail == 0 else ("FAILED" if ok == 0 else "PARTIAL")
    st.session_state.exec_log = elog
    return {"status": s, "total": total, "successful": ok, "failed": fail, "duration": d, "errors": errs, "log": elog}


