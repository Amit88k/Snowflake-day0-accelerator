def clean_sql(raw):
    """Strip markdown fences and empty content from LLM output."""
    text = raw.strip()
    if text.startswith("```"):
        first_nl = text.find("\n")
        if first_nl != -1:
            text = text[first_nl + 1:]
    if text.endswith("```"):
        text = text[:-3]
    lines = text.split("\n")
    cleaned = [l for l in lines if not l.strip().startswith("```")]
    result = "\n".join(cleaned).strip()
    if not result or all(c in (' ', '\n', '\t', ';') for c in result):
        return ""
    return result


def parse_sql_statements(sql_text):
    """Split a multi-statement SQL string on semicolons, preserving statement integrity."""
    stmts = []
    current = []
    for line in sql_text.split("\n"):
        current.append(line)
        stripped = line.strip()
        if stripped.endswith(";"):
            stmt = "\n".join(current).strip()
            if stmt and stmt != ";":
                stmts.append(stmt)
            current = []
    if current:
        remainder = "\n".join(current).strip()
        if remainder and remainder != ";":
            stmts.append(remainder)
    return stmts


def get_stmt_summary(stmt):
    """Return the first 80 chars of a statement for display."""
    first = stmt.strip().split("\n")[0][:80]
    return first


def categorize_statement(stmt):
    """Classify a SQL statement into a human-readable category for the exec log."""
    upper = stmt.strip().upper()
    if upper.startswith("CREATE TRANSIENT DATABASE") or upper.startswith("CREATE DATABASE"): return "Database"
    if upper.startswith("CREATE TRANSIENT SCHEMA") or upper.startswith("CREATE SCHEMA"): return "Schema"
    if upper.startswith("CREATE TABLE") or upper.startswith("CREATE TRANSIENT TABLE"): return "Table"
    if upper.startswith("CREATE FILE FORMAT"): return "File Format"
    if upper.startswith("CREATE WAREHOUSE"): return "Warehouse"
    if upper.startswith("CREATE ROLE"): return "Role"
    if "GRANT" in upper[:20]: return "Grant"
    if upper.startswith("CREATE RESOURCE MONITOR"): return "Resource Monitor"
    if upper.startswith("CREATE TAG") or upper.startswith("ALTER TAG"): return "Tag"
    if "MASKING POLICY" in upper: return "Masking Policy"
    if "ROW ACCESS POLICY" in upper: return "Row Access Policy"
    if "NETWORK POLICY" in upper: return "Network Policy"
    if upper.startswith("CREATE STAGE"): return "Stage"
    if upper.startswith("CREATE PIPE"): return "Pipe"
    if upper.startswith("CREATE STREAM"): return "Stream"
    if upper.startswith("CREATE TASK"): return "Task"
    if upper.startswith("USE "): return "Context"
    return "Other"


