def call_cortex_complete(model, system_prompt, user_prompt):
    """Call Snowflake Cortex Complete and return (response_text, usage_dict)."""
    _dbg(f"Calling Cortex Complete with model={model}, prompt_len={len(user_prompt)}")
    from snowflake.snowpark.functions import col, lit, parse_json
    conversation = [
        {"role": "system", "content": system_prompt},
        {"role": "user", "content": user_prompt}
    ]
    options = {"temperature": 0.1, "max_tokens": 8000}
    conv_json = json.dumps(conversation)
    opts_json = json.dumps(options)
    result = session.sql(
        "SELECT SNOWFLAKE.CORTEX.COMPLETE(?, PARSE_JSON(?), PARSE_JSON(?)) AS response",
        params=[model, conv_json, opts_json]
    ).collect()
    raw_response = result[0]["RESPONSE"]
    if isinstance(raw_response, str):
        try:
            parsed = json.loads(raw_response)
            text = parsed.get("choices", [{}])[0].get("messages", parsed.get("choices", [{}])[0].get("message", ""))
            if isinstance(text, dict):
                text = text.get("content", "")
            elif isinstance(text, str):
                pass
            else:
                text = str(text)
            usage = parsed.get("usage", {})
        except (json.JSONDecodeError, KeyError, IndexError):
            text = raw_response
            usage = {}
    else:
        text = str(raw_response)
        usage = {}
    return text, usage


