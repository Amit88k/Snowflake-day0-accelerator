def build_validation_sql(config):
    """Build a SQL script of SELECT/SHOW queries that verify every expected
    object (database, schema, warehouse, role, table, file format, stage)
    was successfully created.
    """
    _dbg("Building validation SQL")
    project = config.get("project_name", "PROJECT")
    envs = config.get("environments", ["DEV", "PROD"])
    domains = config.get("data_domains", ["RAW", "STAGING", "ANALYTICS"])
    naming = config.get("naming_convention", "<PROJECT>_<ENV>_<OBJECT>")
    roles = config.get("roles", ["ADMIN", "DEVELOPER", "ANALYST", "READER"])
    gov = config.get("governance", {})
    wh_config = config.get("warehouse_config", {})
    sep_etl = config.get("separate_etl_wh", False)
    stmts = []
    stmts.append("-- ============================================")
    stmts.append("-- VALIDATION QUERIES")
    stmts.append("-- Run these after deployment to verify objects")
    stmts.append("-- ============================================")

    # --- Databases ---
    for env in envs:
        db_name = resolve_name(project, env, "DB", naming)
        stmts.append("SELECT '" + db_name + "' AS EXPECTED_DATABASE, COUNT(*) AS FOUND FROM INFORMATION_SCHEMA.DATABASES WHERE DATABASE_NAME = '" + db_name + "';")
    # --- Schemas ---
    for env in envs:
        db_name = resolve_name(project, env, "DB", naming)
        for dom in domains:
            stmts.append("SELECT '" + db_name + "." + dom + "' AS EXPECTED_SCHEMA, COUNT(*) AS FOUND FROM " + db_name + ".INFORMATION_SCHEMA.SCHEMATA WHERE SCHEMA_NAME = '" + dom + "';")

    # --- Warehouses ---
    for env in envs:
        wh_name = resolve_name(project, env, "WH", naming)
        stmts.append("SHOW WAREHOUSES LIKE '" + wh_name + "';")
        if sep_etl:
            etl_name = wh_name.replace("_WH", "_ETL_WH")
            stmts.append("SHOW WAREHOUSES LIKE '" + etl_name + "';")

    # --- Roles ---
    for role in roles:
        full_role = project + "_" + role
        stmts.append("SHOW ROLES LIKE '" + full_role + "';")

    # --- Resource Monitors ---
    if gov.get("resource_monitors", True):
        stmts.append("SHOW RESOURCE MONITORS LIKE '" + project + "%';")

    # --- Masking Policies ---
    if gov.get("masking", True):
        for env in envs:
            db_name = resolve_name(project, env, "DB", naming)
            stmts.append("SHOW MASKING POLICIES IN " + db_name + ";")
            break

    # --- Tags ---
    if gov.get("tagging", True):
        for env in envs:
            db_name = resolve_name(project, env, "DB", naming)
            stmts.append("SHOW TAGS IN " + db_name + ";")
            break
    # --- Tables ---
    initial_tables = config.get("initial_tables", [])
    valid_tables = [t for t in initial_tables if t.get("name")]
    for t in valid_tables:
        for env in envs:
            db_name = resolve_name(project, env, "DB", naming)
            fqn = db_name + "." + t["domain"] + "." + t["name"]
            stmts.append("SELECT '" + fqn + "' AS EXPECTED_TABLE, COUNT(*) AS FOUND FROM " + db_name + ".INFORMATION_SCHEMA.TABLES WHERE TABLE_SCHEMA = '" + t["domain"] + "' AND TABLE_NAME = '" + t["name"] + "';")

    # --- File Formats ---
    file_formats = config.get("file_formats", [])
    valid_formats = [f for f in file_formats if f.get("name")]
    if valid_formats:
        first_env = envs[0] if envs else "DEV"
        ff_db = resolve_name(project, first_env, "DB", naming)
        for f in valid_formats:
            f_schema = f.get("schema", "RAW" if "RAW" in domains else domains[0])
            stmts.append("SELECT '" + ff_db + "." + f_schema + "." + f["name"] + "' AS EXPECTED_FORMAT, COUNT(*) AS FOUND FROM " + ff_db + ".INFORMATION_SCHEMA.FILE_FORMATS WHERE FILE_FORMAT_NAME = '" + f["name"] + "';")

    # --- Stages ---
    stages_cfg = config.get("stages", [])
    valid_stages = [s for s in stages_cfg if s.get("name")]
    for s in valid_stages:
        for env in envs:
            db_name = resolve_name(project, env, "DB", naming)
            s_schema = s.get("schema", "RAW" if "RAW" in domains else domains[0])
            stmts.append("SHOW STAGES LIKE '" + s["name"] + "' IN " + db_name + "." + s_schema + ";")

    _dbg(f"Validation SQL: {len(stmts)} statements")
    return "\n".join(stmts)


